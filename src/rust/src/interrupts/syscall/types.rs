#[repr(u16)]
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum SyscallId {
    // Linux i386 int 0x80 numbers for the syscalls with matching ABI.
    Exit = 1,
    Fork = 2,
    Read = 3,
    Write = 4,
    Open = 5,
    Close = 6,
    WaitPid = 7,
    Unlink = 10,
    Execve = 11,
    Chdir = 12,
    Chmod = 15,
    Lseek = 19,
    GetPid = 20,
    GetUid = 24,
    Kill = 37,
    Mkdir = 39,
    Rmdir = 40,
    Dup = 41,
    Pipe = 42,
    Brk = 45,
    GetGid = 47,
    GetEuid = 49,
    GetEgid = 50,
    Ioctl = 54,
    Fcntl = 55,
    Umask = 60,
    Dup2 = 63,
    GetPpid = 64,
    SigAction = 67,
    GetTimeOfDay = 78,
    LinuxReboot = 88,
    SocketCall = 102,
    Stat = 106,
    Lstat = 107,
    Fstat = 108,
    SigReturn = 119,
    GetDents = 141,
    NanoSleep = 162,
    Chown = 182,
    GetCwd = 183,
    ClockGetTime = 265,
    // PolyOS-private debug/control calls. Keep custom IDs at 500+.
    PrintMemory = 503,
    NetworkInfo = 520,
    NetworkDhcpDiscover = 521,
    NetworkPingGateway = 522,
    NetworkPingIpv4 = 523,
    NetworkDnsQuery = 524,
    NetworkPingName = 525,
    NetworkRecvFromWait = 529,
    SemaphoreCreate = 560,
    SemaphoreWait = 561,
    SemaphoreSignal = 562,
    SemaphoreClose = 563,
    KernelSelfTest = 590,
}

impl SyscallId {
    pub const fn new(v: u32) -> Option<Self> {
        match v {
            1 => Some(Self::Exit),
            2 => Some(Self::Fork),
            3 => Some(Self::Read),
            4 => Some(Self::Write),
            5 => Some(Self::Open),
            6 => Some(Self::Close),
            7 => Some(Self::WaitPid),
            10 => Some(Self::Unlink),
            11 => Some(Self::Execve),
            12 => Some(Self::Chdir),
            15 => Some(Self::Chmod),
            19 => Some(Self::Lseek),
            20 => Some(Self::GetPid),
            24 => Some(Self::GetUid),
            37 => Some(Self::Kill),
            39 => Some(Self::Mkdir),
            40 => Some(Self::Rmdir),
            41 => Some(Self::Dup),
            42 => Some(Self::Pipe),
            45 => Some(Self::Brk),
            47 => Some(Self::GetGid),
            49 => Some(Self::GetEuid),
            50 => Some(Self::GetEgid),
            54 => Some(Self::Ioctl),
            55 => Some(Self::Fcntl),
            60 => Some(Self::Umask),
            63 => Some(Self::Dup2),
            64 => Some(Self::GetPpid),
            67 => Some(Self::SigAction),
            78 => Some(Self::GetTimeOfDay),
            88 => Some(Self::LinuxReboot),
            102 => Some(Self::SocketCall),
            106 => Some(Self::Stat),
            107 => Some(Self::Lstat),
            108 => Some(Self::Fstat),
            119 => Some(Self::SigReturn),
            141 => Some(Self::GetDents),
            162 => Some(Self::NanoSleep),
            182 => Some(Self::Chown),
            183 => Some(Self::GetCwd),
            265 => Some(Self::ClockGetTime),
            503 => Some(Self::PrintMemory),
            520 => Some(Self::NetworkInfo),
            521 => Some(Self::NetworkDhcpDiscover),
            522 => Some(Self::NetworkPingGateway),
            523 => Some(Self::NetworkPingIpv4),
            524 => Some(Self::NetworkDnsQuery),
            525 => Some(Self::NetworkPingName),
            529 => Some(Self::NetworkRecvFromWait),
            560 => Some(Self::SemaphoreCreate),
            561 => Some(Self::SemaphoreWait),
            562 => Some(Self::SemaphoreSignal),
            563 => Some(Self::SemaphoreClose),
            590 => Some(Self::KernelSelfTest),
            _ => None,
        }
    }
}
